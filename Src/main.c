/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
  Can_Config();
  /*##-2- Start the Reception process and enable reception interrupt #########*/
  if (HAL_CAN_Receive_IT(&hcan, CAN_FIFO0) != HAL_OK)
  {
    /* Reception Error */
    Error_Handler();
  }

  /* Infinite loop */
  for(;;)
  {
		if(1){
			while (HAL_GPIO_ReadPin(BUTTON1_GPIO_Port, BUTTON1_Pin) == KEY_PRESSED)
			{
				++ubKeyNumber;
				if (ubKeyNumber == 0x4) {
					ubKeyNumber = 0x00;
				}
				// Set the data to be tranmitted 
			  hcan.pTxMsg->StdId = 0x321; // Switch toggle data
				hcan.pTxMsg->DLC = 2;
				hcan.pTxMsg->Data[0] = ubKeyNumber;
				hcan.pTxMsg->Data[1] = 0xAD;
					
				//##-3- Start the Transmission process ###############################
				if (HAL_CAN_Transmit(&hcan, 10) != HAL_OK)
				{
					// Transmition Error
					Error_Handler();
				}
				osDelay(10);
					
				while (HAL_GPIO_ReadPin(BUTTON1_GPIO_Port, BUTTON1_Pin) != KEY_NOT_PRESSED)
				{
					osDelay(1);
				}
			}
		}
		else {
			// Read temperature sensor
			
		}
		osDelay(1);
  }
  /* USER CODE END 5 */ 
}

